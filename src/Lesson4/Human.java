package Lesson4;

public class Human {
    public int weight;
    public int age;
    public String firstName;
    public String lastName;
    public int height;
    public Human(int weight, int age, String first_name, String last_name, int height) {
        this.height = height;
        this.age = age;
        this.firstName = first_name;
        this.lastName = last_name;
        this.weight = weight;
    }
}
