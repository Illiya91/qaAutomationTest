package Lesson4;

public class Human {
    public int weight;
    public int age;
    public String first_name;
    public String last_name;
    public int height;
    public Human(int weight, int age, String first_name, String last_name, int height) {
        this.height = height;
        this.age = age;
        this.first_name = first_name;
        this.last_name = last_name;
        this.weight = weight;
    }
}
